                                                    <h3 data-aos="fade-left" data-aos-easing="ease" data-aos-delay="300">FIRE BEHAVIOUR</h3>
                                                  <p>By applying heat, shearing and pressure, the raw material is heated during the extrusion process to convert it into a viscious fluid form. A rotating screw presses this molten material with the help of a shaping tool-fishtail nozzle. The surface of the calendar rollers generally has a high-gloss finish. It essentially determines the surface quality of the sheet. The shaping process is complete once the calendar has performed its smoothening task. After the extruded material has cooled down on the roller conveyor, sheets are produced by being cut to the desired length in the separating unit before being off-loaded onto transport pallets by the destacker.</p>
                                                  <p>Alstone can be classed as a standard building material with normal flammability as per DIN 4102 B1.

                                                ALSTONE can also supply a version that meets International Maritime Organization’s (IMO) specifications for low maintenance. 
                                                .As the material meets the necessary requirements,ALSTONE IMO is suitable for boat- and ship¬ building purposes and can be used on bulkheads, walls, ceilings, linings or floor coverings.
                                                
                                                In an event of a fire, hydrogen chloride gas and other broken down by-products may be released. For this reason, ALSTONE should not be burnt without taking suitable emission control precautions. Offcuts and/or sanding dust, as with any other thermo plastic material, should be properly disposed.
