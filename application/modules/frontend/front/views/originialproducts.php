                                          <p>Alstone sheets are produced by means of the extrusion method. Sheet extrusion is a thermoplastic processing method.
                                             The plant consists of an extruder which is a tool that is also referred to as a die with a fishtail design for shaping purposes, a triple roller calendar to smooth the extruded material, a cooling section, a haul-off and a separating unit (saw or cutting device) and a de-stacker.
